    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        ImageIcon imagen;
        Pelicula peliObj;
        if (operac == 2) {
            txtId.setEditable(false);
            peliObj = almacenaPeli.getRegistro(posi);
            txtId.setText(String.valueOf(peliObj.getId()));
            txtTitulo.setText(peliObj.getTitulo());
            cmbGenero.setSelectedItem(peliObj.getGenero());
            txtDuracion.setText(String.valueOf(peliObj.getDuracion()));
            cmbIdioma.setSelectedItem(peliObj.getIdioma());
            cmbClasif.setSelectedItem(peliObj.getClasif());
            if (rdb2D.getText().equals(peliObj.getTipo())) {
                rdb2D.setSelected(true);
            } else {
                rdb3D.setSelected(true);
            }
//            if (idImg.equals("")) {
//                idImg="defaultPic.png";
//            }

            imagen = new ImageIcon(getClass().getResource("/img/" + peliObj.getImagen()));
            ImageIcon imgAjust = new ImageIcon(imagen.getImage().getScaledInstance(lblImagen.getWidth(), lblImagen.getHeight(), Image.SCALE_DEFAULT));
            imgAjust.getImage().flush();

            lblImagen.setIcon(imgAjust);
        }
    }//GEN-LAST:event_formWindowActivated

