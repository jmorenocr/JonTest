    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String valTipo = "";
        if (!txtId.getText().equals("") && !txtTitulo.getText().equals("")
                && cmbGenero.getSelectedIndex() != 0 && cmbIdioma.getSelectedIndex() != 0
                && cmbClasif.getSelectedIndex() != 0 && (rdb2D.isSelected() || rdb3D.isSelected())) {
            if (rdb2D.isSelected()) {
                valTipo = rdb2D.getText();
            } else if (rdb3D.isSelected()) {
                valTipo = rdb3D.getText();
            }
            int durac;
            if (txtDuracion.getText().equals("")) {
                durac = 0;
            } else {
                durac = Integer.parseInt(txtDuracion.getText());
            }
            Pelicula peliObj = new Pelicula(Integer.parseInt(txtId.getText()), txtTitulo.getText(),
                    String.valueOf(cmbGenero.getSelectedItem()), durac,
                    String.valueOf(cmbIdioma.getSelectedItem()), String.valueOf(cmbClasif.getSelectedItem()),
                    valTipo, idImg);  
            if (idImg.equals("")){
                peliObj.setImagen("defaultPic.png");
            }
            if (operac == 1) {   //Se guarda un nuevo registro
                if (!almacenaPeli.yaExiste(Integer.parseInt(txtId.getText()))) {

                    if (almacenaPeli.insertPelicula(peliObj)) {
                        JOptionPane.showMessageDialog(this, "Registro almacenado");
                    } else {
                        JOptionPane.showMessageDialog(this, "Vector lleno, borre registros");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "El id ya existe");
                }
            } else { //Editar un registro existente

                if (almacenaPeli.editaPelicula(posi, peliObj)) {
                    JOptionPane.showMessageDialog(this, "Resgistro Editado");
                    txtId.setEditable(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Error de edicion");
                }
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Faltan Datos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

