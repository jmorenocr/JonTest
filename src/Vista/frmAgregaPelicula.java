    private void btnImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImagenActionPerformed
        // TODO add your handling code here:

        String destino;

        String origen;
        if (!txtId.getText().equals("")) {
            idImg = "img" + txtId.getText() + ".jpg";
            SelectorArchivos chooser = new SelectorArchivos(null, true);
            chooser.setVisible(true);

            origen = chooser.path;
            String rutaFondo = System.getProperty("java.class.path") + "\\img\\" + idImg;
            String rut[] = rutaFondo.split(";");
            destino = rut[1];

            Path FROM = Paths.get(origen);
            Path TO = Paths.get(destino);
            //sobreescribir el fichero de destino, si existe, y copiar los atributos, 
            //incluyendo los permisos rwx
            CopyOption[] options = new CopyOption[]{
                StandardCopyOption.REPLACE_EXISTING,
                StandardCopyOption.COPY_ATTRIBUTES};
            try {
                Files.copy(FROM, TO.toAbsolutePath(), options);
            } catch (IOException ex) {

            }
            ImageIcon imagen = new ImageIcon(getClass().getResource("/img/" + idImg));
            ImageIcon imgAjust = new ImageIcon(imagen.getImage().getScaledInstance(
                    lblImagen.getWidth(), lblImagen.getHeight(), Image.SCALE_DEFAULT));
            imgAjust.getImage().flush();

            lblImagen.setIcon(imgAjust);

        } else {
            JOptionPane.showMessageDialog(this, "Se debe establecer el id");
        }
    }//GEN-LAST:event_btnImagenActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        ImageIcon imagen;
        Pelicula peliObj;
        if (operac == 2) {
            txtId.setEditable(false);
            peliObj = almacenaPeli.getRegistro(posi);
            txtId.setText(String.valueOf(peliObj.getId()));
            txtTitulo.setText(peliObj.getTitulo());
            cmbGenero.setSelectedItem(peliObj.getGenero());
            txtDuracion.setText(String.valueOf(peliObj.getDuracion()));
            cmbIdioma.setSelectedItem(peliObj.getIdioma());
            cmbClasif.setSelectedItem(peliObj.getClasif());
            if (rdb2D.getText().equals(peliObj.getTipo())) {
                rdb2D.setSelected(true);
            } else {
                rdb3D.setSelected(true);
            }
//            if (idImg.equals("")) {
//                idImg="defaultPic.png";
//            }

            imagen = new ImageIcon(getClass().getResource("/img/" + peliObj.getImagen()));
            ImageIcon imgAjust = new ImageIcon(imagen.getImage().getScaledInstance(lblImagen.getWidth(), lblImagen.getHeight(), Image.SCALE_DEFAULT));
            imgAjust.getImage().flush();

            lblImagen.setIcon(imgAjust);
        }
    }//GEN-LAST:event_formWindowActivated

